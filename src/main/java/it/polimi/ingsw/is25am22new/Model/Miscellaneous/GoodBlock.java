package it.polimi.ingsw.is25am22new.Model.Miscellaneous;

/**
 * The GoodBlock enum represents different types of blocks used within the game system.
 * Each block type can be managed and interacted with in various ways, such as being
 * deposited into or withdrawn from a bank, or being associated with certain game mechanics.
 *
 * The available GoodBlock types are:
 * - REDBLOCK = 4 points
 * - YELLOWBLOCK = 3 points
 * - GREENBLOCK = 2 points
 * - BLUEBLOCK = 1 point
 */
public enum GoodBlock {
    REDBLOCK,
    YELLOWBLOCK,
    GREENBLOCK,
    BLUEBLOCK
}
